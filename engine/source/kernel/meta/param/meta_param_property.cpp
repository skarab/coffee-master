namespace coffee
{
    //-META---------------------------------------------------------------------------------------//

    COFFEE_BeginType(meta::ParamProperty);
    COFFEE_EndType();

namespace meta
{
    //-CONSTRUCTORS-------------------------------------------------------------------------------//

    ParamProperty::ParamProperty() :
        _Flags(0)
    {
    }

    //--------------------------------------------------------------------------------------------//

    ParamProperty::ParamProperty(uint32 mode, uint32 flags) :
        Param(mode),
        _Flags(flags)
    {
    }

    //--------------------------------------------------------------------------------------------//

    ParamProperty::~ParamProperty()
    {
    }

    //-ACCESSORS----------------------------------------------------------------------------------//

    void ParamProperty::SetFlags(uint32 flags)
    {
        _Flags = flags;
    }

    //--------------------------------------------------------------------------------------------//

    uint32 ParamProperty::GetFlags() const
    {
        return _Flags;
    }

}
}
