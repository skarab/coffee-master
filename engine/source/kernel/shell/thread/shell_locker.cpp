namespace coffee
{
namespace shell
{
    //-CONSTRUCTORS-------------------------------------------------------------------------------//

    Locker::Locker(Mutex& mutex) :
        _Mutex(&mutex)
    {
        _Mutex->Lock();
    }

    //--------------------------------------------------------------------------------------------//

    Locker::~Locker()
    {
        _Mutex->Unlock();
    }

}
}
