    //-QUERIES------------------------------------------------------------------------------------//

    basic::String PropertyInt32::_GetTextFromValue(int32 value) const
    {
        return basic::String("%d", value);
    }

    //--------------------------------------------------------------------------------------------//

    int32 PropertyInt32::_GetValueFromText(const basic::String& text) const
    {
        return text.GetInteger();
    }

